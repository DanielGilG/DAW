-- Ejercicio 1
CREATE OR REPLACE TRIGGER trg_salario_minimo
BEFORE INSERT ON HR.EMPLOYEES
BEGIN
	IF :NEW.salary < 1000 THEN
		RAISE_APPLICATION_ERROR(-200001,"Rácano");
	END IF;
END;
/

