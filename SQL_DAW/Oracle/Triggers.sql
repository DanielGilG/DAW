CREATE OR REPLACE TRIGGER trg_salario_minimo
BEFORE DELETE ON HR.EMPLOYEES
FOR EACH ROW
BEGIN
	IF :OLD.DEPARTMENT_ID = 90 THEN
		RAISE_APPLICATION_ERROR(-200001,"Rácano");
	END IF;
END;
/

